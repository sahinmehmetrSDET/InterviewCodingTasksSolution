package Numbers.OddAndEven;

import Strings.SameLetters.Solution;

public class Solution_OddAndEven {
  //  Write  a method which can identifies given number is even or odd

  //  EX:

  //  identify(5) ->  "Odd"

  //  identify(6) ->  "Even"



   // Solution :

    public  static  String  identifyOddEven( int  n ) {

        return  n%2==0 ? "Even" : "odd" ;

    }

}
